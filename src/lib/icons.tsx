// File type icons for the file tree
type Ext = string

export function FileIcon({ name, className }: { name: string; className?: string }) {
  const ext = name.split('.').pop()?.toLowerCase() || ''
  const colorMap: Record<string, string> = {
    js: '#f7df1e', ts: '#3178c6', jsx: '#61dafb', tsx: '#3178c6',
    html: '#e34f26', css: '#1572b6', json: '#292929', py: '#3776ab',
    rs: '#dea584', go: '#00add8', cpp: '#00599c', c: '#a8b9cc',
    java: '#007396', rb: '#cc342d', php: '#777bb4', swift: '#f05138',
    kt: '#7f52ff', dart: '#0175c2', cs: '#178600', scala: '#dc322f',
    md: '#6a737d', sql: '#e38c00', yaml: '#6a737d', yml: '#6a737d',
    sh: '#4eaa25', bash: '#4eaa25', lua: '#000080', r: '#198ce7',
  }
  const color = colorMap[ext]
  if (color) {
    return (
      <svg viewBox="0 0 20 20" className={className || 'w-4 h-4'} fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round">
        <path d="M13 2H5C4.447 2 4 2.447 4 3V17C4 17.553 4.447 18 5 18H15C15.553 18 16 17.553 16 17V6L13 2Z" />
        <path d="M13 2V6H17" />
        <text x="10" y="13" textAnchor="middle" fontSize="7" fontWeight="700" fill={color} stroke="none">{ext.includes('s') && !['cs','rs','js','ts'].includes(ext) ? ext.replace('s','') : ext[0]}</text>
      </svg>
    )
  }
  const imageExts = ['png', 'jpg', 'jpeg', 'gif', 'svg', 'webp', 'ico', 'bmp']
  if (imageExts.includes(ext)) {
    return (
      <svg viewBox="0 0 20 20" fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round" className={className || 'w-4 h-4'}>
        <rect x="2" y="2" width="16" height="16" rx="2" /><circle cx="7" cy="7" r="1.5" /><path d="M2 14L7 9L11 13" /><path d="M11 11L14 8L18 12" />
      </svg>
    )
  }
  return (
    <svg viewBox="0 0 20 20" fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round" className={className || 'w-4 h-4'}>
      <path d="M13 2H5C4.447 2 4 2.447 4 3V17C4 17.553 4.447 18 5 18H15C15.553 18 16 17.553 16 17V6L13 2Z" />
      <path d="M13 2V6H17" />
    </svg>
  )
}
